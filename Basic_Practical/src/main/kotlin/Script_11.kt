//11. Write a program to calculate area and perimeter of the rectangle
fun main() {
    var length = 10
    var width = 5

    var area = length * width

    var perimeter = 2 * (length  + width)

    println("Dimensions of the rectangle:")
    println("Area of the rectangle: $area square units")
    println("Perimeter of the rectangle: $perimeter units")
}
